    <!--<p class="title">爱上街管理费及保证金规则</p>-->
    <b>1.总则</b><br>
    1.1.      为明确爱上街平台管理费以及商家保证金的缴存、调整、补缴、赔付、扣划及退回等事宜，促进商家更好地服务爱上街用户，特制定本规则。<br>
    1.2.      保证金是指商家向平台缴存的，用以保证其提供优质的商品和服务，以及严格按照《爱上街平台合作协议》（以下简称“平台协议”）及各项平台规则合规经营的资金,并在商家出现违规情形时用于向用户进行赔付、赔偿平台的损失或承担其他违约责任。<br>
    1.3.      商家保证金的缴存方式以爱上街规定为准，保证金缴存、补缴、赔付、退回等过程中所发生的银行手续费等第三方费用由商家自行承担。<br>
    1.4.      商家应保证其用于缴纳保证金的资金来源合法，且商家对该等资金依法享有处分权；若商家保证金系由第三方代为支付的，商家应保证该第三方清楚知晓所支付款项的性质、用途等情况且同意代为支付，并保证平台不会受到任何第三方就商家保证金主张其享有任何权利或以任何理由要求返还。<br>
    1.5.      对于商家缴存的保证金，爱上街无须向商家支付任何利息等孳息。<br>
    1.6.  爱上街负责爱上街平台的日常维护、技术支持，保证平台的正常运作，商家充分理解并确认，爱上街有权收取平台相关服务费用----即平台管理费。<br>
    <b>2.商家保证金类型</b><br>
    2.1.      店铺保证金，是指商家入驻爱上街平台开设店铺，按照本规则第3条规定缴存的保证金。<br>
    2.2.      活动保证金，是指店铺申请参加活动前按照本规则第4.2条及平台相关活动规则规定的标准缴存的保证金。<br>
    <b>3.店铺保证金规则</b><br>
    3.1.      店铺保证金缴存<br>
    3.1.1. 店铺保证金缴存方式以如下表为准，商家需在入驻时一次性缴存足额的店铺保证金<br>
    3.1.2. 各类店铺应缴存的基础店铺保证金标准如下表一所示：<br>
